    public void getData( ){
     
     model.getDataVector( ).removeAllElements( );
     model.fireTableDataChanged( );
     kon =koneksi.koneksiDb();
     try{
           
//membuat statemen untuk memanggil data table detail penerimaan
              
           Statement stat = kon.createStatement( );
           String sql     = "Select * from detail_penerimaan";
           ResultSet res  = stat.executeQuery(sql);
     
//pengecekan terhadap data detail penerimaan
           while(res.next ()){
                Object[] obj = new Object[8];
                obj[0] = res.getString("no_order");
                obj[1] = res.getString("kd_jenis");
                obj[2] = res.getString("jenis_cucian");
                obj[3] = res.getString("harga");
                obj[4] = res.getString("berat");
                obj[5] = res.getString("qty");
                obj[6] = res.getString("ket");
                obj[7] = res.getString("pengerjaan");
                model.addRow(obj);
            }
      }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage() );
      }      
    }
    
    
